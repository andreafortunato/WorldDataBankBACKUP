package it.apperol.group.worlddatabank.myobjects;

public class PlotObj {
    private String year;
    private String value;

    public PlotObj(String year, String value) {
        this.year = year;
        this.value = value;
    }

    public String getYear() {
        return year;
    }

    public String getValue() {
        return value;
    }
}
